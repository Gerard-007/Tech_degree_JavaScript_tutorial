// Function Declaration
function divide(a, b) {
	return a / b;
}

// Function Expression
var divide2 = function(a, b) {
	return a / b;
}

// Arrow Function Expression
const divide3 = (a, b) => {
	return a / b;
}

// Arrow Function Expression - Concised
const divide4 = (a, b) => a / b;